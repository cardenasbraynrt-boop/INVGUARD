create or replace function public.soft_delete_producto(
  p_empresa_id bigint,
  p_producto_id bigint
)
returns table (
  id bigint,
  deleted_at timestamp with time zone
)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_producto public.productos;
begin
  if not public.es_admin_empresa(p_empresa_id) then
    raise exception 'Solo un ADMIN puede eliminar productos';
  end if;

  update public.productos p
    set activo = false,
        deleted_at = now(),
        deleted_by = auth.uid(),
        updated_by = auth.uid()
  where p.id = p_producto_id
    and p.empresa_id = p_empresa_id
    and p.deleted_at is null
  returning * into v_producto;

  if v_producto.id is null then
    raise exception 'Producto no encontrado o ya eliminado';
  end if;

  return query select v_producto.id, v_producto.deleted_at;
end;
$$;

create or replace function public.registrar_movimiento_lote(
  p_empresa_id bigint,
  p_producto_id bigint,
  p_tipo text,
  p_cantidad numeric,
  p_observacion text default '',
  p_fecha_vencimiento date default null,
  p_codigo_lote text default '',
  p_proveedor text default ''
)
returns table (
  movimiento_id bigint,
  stock_actual numeric
)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_stock numeric;
  v_nuevo_stock numeric;
  v_movimiento_id bigint;
  v_restante numeric;
  v_consumir numeric;
  v_lote public.producto_lotes;
begin
  if not public.tiene_acceso_empresa(p_empresa_id) then
    raise exception 'No tienes acceso a esta empresa';
  end if;

  if p_cantidad <= 0 then
    raise exception 'La cantidad debe ser mayor a cero';
  end if;

  select p.stock
    into v_stock
  from public.productos p
  where p.id = p_producto_id
    and p.empresa_id = p_empresa_id
    and p.activo = true
    and p.deleted_at is null
  for update;

  if not found then
    raise exception 'Producto no encontrado o inactivo';
  end if;

  if p_tipo = 'ENTRADA' then
    v_nuevo_stock := v_stock + p_cantidad;
  elsif p_tipo = 'SALIDA' then
    v_nuevo_stock := v_stock - p_cantidad;
  else
    raise exception 'Tipo de movimiento invalido';
  end if;

  if v_nuevo_stock < 0 then
    raise exception 'Stock insuficiente';
  end if;

  update public.productos p
    set stock = v_nuevo_stock,
        updated_by = auth.uid()
  where p.id = p_producto_id
    and p.empresa_id = p_empresa_id;

  if p_tipo = 'ENTRADA'
    and (
      p_fecha_vencimiento is not null
      or nullif(trim(p_codigo_lote), '') is not null
      or nullif(trim(p_proveedor), '') is not null
    ) then
    insert into public.producto_lotes (
      empresa_id,
      producto_id,
      codigo_lote,
      proveedor,
      fecha_ingreso,
      fecha_vencimiento,
      cantidad_inicial,
      cantidad_actual,
      created_by
    )
    values (
      p_empresa_id,
      p_producto_id,
      nullif(trim(p_codigo_lote), ''),
      nullif(trim(p_proveedor), ''),
      current_date,
      p_fecha_vencimiento,
      p_cantidad,
      p_cantidad,
      auth.uid()
    );
  end if;

  if p_tipo = 'SALIDA' then
    v_restante := p_cantidad;

    for v_lote in
      select pl.*
      from public.producto_lotes pl
      where pl.empresa_id = p_empresa_id
        and pl.producto_id = p_producto_id
        and pl.cantidad_actual > 0
      order by pl.fecha_vencimiento asc nulls last, pl.created_at asc
      for update
    loop
      exit when v_restante <= 0;

      v_consumir := least(v_restante, v_lote.cantidad_actual);

      update public.producto_lotes pl
        set cantidad_actual = pl.cantidad_actual - v_consumir
      where pl.id = v_lote.id;

      v_restante := v_restante - v_consumir;
    end loop;
  end if;

  insert into public.movimientos (
    empresa_id,
    producto_id,
    tipo,
    cantidad,
    observacion,
    created_by
  )
  values (
    p_empresa_id,
    p_producto_id,
    p_tipo,
    p_cantidad,
    nullif(trim(p_observacion), ''),
    auth.uid()
  )
  returning id into v_movimiento_id;

  return query select v_movimiento_id, v_nuevo_stock;
end;
$$;

