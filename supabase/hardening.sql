create or replace function public.audit_row_change()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  v_empresa_id bigint;
  v_record_id text;
  v_actor_email text;
begin
  if tg_op = 'DELETE' then
    v_empresa_id := (to_jsonb(old) ->> 'empresa_id')::bigint;
    v_record_id := to_jsonb(old) ->> 'id';
  else
    v_empresa_id := (to_jsonb(new) ->> 'empresa_id')::bigint;
    v_record_id := to_jsonb(new) ->> 'id';
  end if;

  select au.email::text
    into v_actor_email
  from auth.users au
  where au.id = auth.uid();

  insert into public.audit_logs (
    empresa_id,
    actor_id,
    actor_email,
    accion,
    tabla,
    registro_id,
    antes,
    despues
  )
  values (
    v_empresa_id,
    auth.uid(),
    v_actor_email,
    tg_op,
    tg_table_name,
    v_record_id,
    case when tg_op in ('UPDATE', 'DELETE') then to_jsonb(old) else null end,
    case when tg_op in ('INSERT', 'UPDATE') then to_jsonb(new) else null end
  );

  return coalesce(new, old);
end;
$$;

