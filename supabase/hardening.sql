create or replace function public.es_gestor_inventario(p_empresa_id bigint)
returns boolean
language sql
security definer
set search_path = public
as $$
  select public.es_super_admin()
    or exists (
      select 1
      from public.empresa_usuarios eu
      join public.empresas e on e.id = eu.empresa_id
      where eu.empresa_id = p_empresa_id
        and eu.user_id = auth.uid()
        and eu.rol in ('ADMIN', 'SUPERVISOR')
        and e.estado in ('BETA', 'ACTIVO')
    );
$$;

