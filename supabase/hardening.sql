with duplicated_products as (
  select
    p.id,
    p.empresa_id,
    p.codigo,
    row_number() over (
      partition by p.empresa_id, lower(p.codigo)
      order by p.id
    ) as duplicate_order
  from public.productos p
  where p.codigo is not null
    and trim(p.codigo) <> ''
    and coalesce(p.activo, true) = true
    and p.deleted_at is null
)
update public.productos p
  set codigo = concat(p.codigo, '-', duplicated_products.duplicate_order),
      updated_at = now()
from duplicated_products
where p.id = duplicated_products.id
  and duplicated_products.duplicate_order > 1;

create unique index if not exists productos_empresa_codigo_active_unique
  on public.productos (empresa_id, lower(codigo))
  where codigo is not null
    and trim(codigo) <> ''
