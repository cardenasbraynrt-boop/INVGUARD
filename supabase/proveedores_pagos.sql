create or replace function public.preparar_cuenta_por_pagar()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  if not exists (
    select 1
    from public.proveedores p
    where p.id = new.proveedor_id
      and p.empresa_id = new.empresa_id
      and p.activo = true
  ) then
    raise exception 'Proveedor no encontrado o inactivo';
  end if;

  new.saldo = new.total;
  new.estado = 'PENDIENTE';
  new.created_by = coalesce(new.created_by, auth.uid());
  new.updated_by = auth.uid();
  new.updated_at = now();
  return new;
end;
$$;

