create or replace function public.registrar_pago_proveedor(
  p_empresa_id bigint,
  p_cuenta_id bigint,
  p_monto numeric,
  p_fecha_pago date default current_date,
  p_medio text default 'TRANSFERENCIA',
  p_referencia text default '',
  p_notas text default ''
)
returns table (
  pago_id bigint,
  saldo_actual numeric,
  estado_actual text
)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_cuenta public.cuentas_por_pagar;
  v_pago_id bigint;
  v_saldo numeric;
  v_estado text;
  v_medio text := upper(trim(p_medio));
begin
  if not public.es_gestor_inventario(p_empresa_id) then
    raise exception 'No tienes permiso para registrar pagos';
  end if;

  if p_monto is null or p_monto <= 0 then
    raise exception 'El monto debe ser mayor a cero';
  end if;

  if p_fecha_pago > current_date then
    raise exception 'La fecha del pago no puede estar en el futuro';
  end if;

  if v_medio not in ('EFECTIVO', 'TRANSFERENCIA', 'TARJETA', 'CHEQUE', 'OTRO') then
    raise exception 'Medio de pago invalido';
  end if;

  select cpp.*
    into v_cuenta
  from public.cuentas_por_pagar cpp
  where cpp.id = p_cuenta_id
    and cpp.empresa_id = p_empresa_id
  for update;

  if v_cuenta.id is null then
    raise exception 'Cuenta por pagar no encontrada';
  end if;

  if v_cuenta.estado in ('PAGADO', 'ANULADO') then
    raise exception 'Esta cuenta ya no acepta pagos';
  end if;

  if p_monto > v_cuenta.saldo then
    raise exception 'El pago supera el saldo pendiente';
  end if;

  v_saldo := v_cuenta.saldo - p_monto;
  v_estado := case when v_saldo = 0 then 'PAGADO' else 'PARCIAL' end;

  insert into public.pagos_proveedor (
    empresa_id,
    cuenta_id,
    monto,
    fecha_pago,
    medio,
    referencia,
    notas,
    created_by
  )
  values (
    p_empresa_id,
    p_cuenta_id,
    p_monto,
    coalesce(p_fecha_pago, current_date),
    v_medio,
    nullif(trim(p_referencia), ''),
    nullif(trim(p_notas), ''),
    auth.uid()
  )
  returning id into v_pago_id;

  update public.cuentas_por_pagar cpp
    set saldo = v_saldo,
        estado = v_estado,
        updated_by = auth.uid()
  where cpp.id = v_cuenta.id;

  return query select v_pago_id, v_saldo, v_estado;
end;
$$;

