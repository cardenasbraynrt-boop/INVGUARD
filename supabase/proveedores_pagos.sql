-- InvGuard: proveedores, cuentas por pagar y abonos.
-- Ejecutar despues de supabase/hardening.sql.

create table if not exists public.proveedores (
  id bigint generated by default as identity primary key,
  empresa_id bigint not null references public.empresas(id) on delete cascade,
  nombre text not null,
  documento text,
  contacto text,
  telefono text,
  email text,
  notas text,
  activo boolean not null default true,
  created_by uuid,
  updated_by uuid,
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now()
);

create table if not exists public.cuentas_por_pagar (
  id bigint generated by default as identity primary key,
  empresa_id bigint not null references public.empresas(id) on delete cascade,
  proveedor_id bigint not null references public.proveedores(id) on delete restrict,
  concepto text not null,
  documento_ref text,
  fecha_emision date not null default current_date,
  fecha_vencimiento date not null check (fecha_vencimiento >= fecha_emision),
  total numeric(14, 2) not null check (total > 0),
  saldo numeric(14, 2) not null check (saldo >= 0 and saldo <= total),
  estado text not null default 'PENDIENTE'
    check (estado in ('PENDIENTE', 'PARCIAL', 'PAGADO', 'ANULADO')),
  recordatorio_dias integer not null default 3 check (recordatorio_dias between 0 and 90),
  notas text,
  created_by uuid,
  updated_by uuid,
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now()
);

create table if not exists public.pagos_proveedor (
  id bigint generated by default as identity primary key,
  empresa_id bigint not null references public.empresas(id) on delete cascade,
  cuenta_id bigint not null references public.cuentas_por_pagar(id) on delete restrict,
  monto numeric(14, 2) not null check (monto > 0),
  fecha_pago date not null default current_date,
  medio text not null default 'TRANSFERENCIA'
    check (medio in ('EFECTIVO', 'TRANSFERENCIA', 'TARJETA', 'CHEQUE', 'OTRO')),
  referencia text,
  notas text,
  created_by uuid,
  created_at timestamp with time zone not null default now()
);

create unique index if not exists proveedores_empresa_nombre_unique
  on public.proveedores (empresa_id, lower(nombre))
  where activo = true;

create index if not exists proveedores_empresa_activo_idx
  on public.proveedores (empresa_id, activo, nombre);

create index if not exists cuentas_pagar_empresa_estado_vencimiento_idx
  on public.cuentas_por_pagar (empresa_id, estado, fecha_vencimiento);

create index if not exists cuentas_pagar_proveedor_idx
  on public.cuentas_por_pagar (proveedor_id, fecha_vencimiento desc);

create index if not exists pagos_proveedor_cuenta_fecha_idx
  on public.pagos_proveedor (cuenta_id, fecha_pago desc);

alter table public.proveedores enable row level security;
alter table public.cuentas_por_pagar enable row level security;
alter table public.pagos_proveedor enable row level security;

