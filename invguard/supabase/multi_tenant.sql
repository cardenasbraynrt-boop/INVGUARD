create or replace function public.es_super_admin()
returns boolean
language sql
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.app_admins aa
    where aa.user_id = auth.uid()
  );
$$;

create or replace function public.es_miembro_empresa(p_empresa_id bigint)
returns boolean
language sql
security definer
set search_path = public
as $$
  select public.es_super_admin()
    or exists (
      select 1
      from public.empresa_usuarios eu
      where eu.empresa_id = p_empresa_id
        and eu.user_id = auth.uid()
    );
$$;

create or replace function public.empresa_habilitada(p_empresa_id bigint)
returns boolean
language sql
security definer
set search_path = public
as $$
  select public.es_super_admin()
    or exists (
      select 1
      from public.empresas e
      where e.id = p_empresa_id
        and e.estado in ('BETA', 'ACTIVO')
    );
$$;

create or replace function public.tiene_acceso_empresa(p_empresa_id bigint)
returns boolean
language sql
security definer
set search_path = public
as $$
  select public.es_miembro_empresa(p_empresa_id)
    and public.empresa_habilitada(p_empresa_id);
$$;

create or replace function public.es_admin_empresa(p_empresa_id bigint)
returns boolean
language sql
security definer
set search_path = public
as $$
  select public.es_super_admin()
    or exists (
      select 1
      from public.empresa_usuarios eu
      join public.empresas e on e.id = eu.empresa_id
      where eu.empresa_id = p_empresa_id
        and eu.user_id = auth.uid()
        and eu.rol = 'ADMIN'
        and e.estado in ('BETA', 'ACTIVO')
    );
$$;

