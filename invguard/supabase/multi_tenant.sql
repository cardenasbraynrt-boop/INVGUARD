-- InvGuard multi-tenant SaaS schema
-- Ejecutar en Supabase SQL Editor.
-- Este script separa los datos por empresa y usuario con RLS.

create extension if not exists pgcrypto;

create table if not exists public.empresas (
  id bigint generated by default as identity primary key,
  owner_id uuid references auth.users(id) on delete cascade,
  nombre text not null,
  rubro text,
  ciudad text,
  estado text not null default 'ACTIVA',
  created_at timestamp with time zone not null default now()
);

alter table public.empresas
  add column if not exists owner_id uuid references auth.users(id) on delete cascade;

alter table public.empresas
  add column if not exists rubro text;

alter table public.empresas
  add column if not exists ciudad text;

alter table public.empresas
  add column if not exists estado text not null default 'ACTIVA';

alter table public.empresas
  add column if not exists created_at timestamp with time zone not null default now();

create table if not exists public.empresa_usuarios (
  id bigint generated by default as identity primary key,
  empresa_id bigint not null references public.empresas(id) on delete cascade,
  user_id uuid not null references auth.users(id) on delete cascade,
  email text,
  rol text not null default 'ADMIN' check (rol in ('ADMIN', 'SUPERVISOR', 'EMPLEADO')),
  created_at timestamp with time zone not null default now(),
  unique (empresa_id, user_id)
);

alter table public.empresa_usuarios
  add column if not exists email text;

create table if not exists public.invitaciones_empresa (
  id uuid primary key default gen_random_uuid(),
  empresa_id bigint not null references public.empresas(id) on delete cascade,
  email text not null,
  rol text not null default 'EMPLEADO' check (rol in ('ADMIN', 'SUPERVISOR', 'EMPLEADO')),
  token text not null unique default encode(gen_random_bytes(18), 'hex'),
  estado text not null default 'PENDIENTE' check (estado in ('PENDIENTE', 'ACEPTADA', 'CANCELADA')),
  created_by uuid not null references auth.users(id) on delete cascade,
  accepted_by uuid references auth.users(id) on delete set null,
  expires_at timestamp with time zone not null default (now() + interval '7 days'),
  created_at timestamp with time zone not null default now()
);

create table if not exists public.productos (
  id bigint generated by default as identity primary key,
  empresa_id bigint not null references public.empresas(id) on delete cascade,
  codigo text,
  nombre text not null,
  categoria text,
  stock numeric not null default 0 check (stock >= 0),
  stock_minimo numeric not null default 0 check (stock_minimo >= 0),
  precio_compra numeric not null default 0 check (precio_compra >= 0),
  precio_venta numeric not null default 0 check (precio_venta >= 0),
  created_at timestamp with time zone not null default now()
);

alter table public.productos
