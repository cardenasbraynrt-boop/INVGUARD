security definer
set search_path = public
as $$
declare
  v_stock numeric;
  v_nuevo_stock numeric;
  v_movimiento_id bigint;
begin
  if not public.tiene_acceso_empresa(p_empresa_id) then
    raise exception 'No tienes acceso a esta empresa';
  end if;

  if p_cantidad <= 0 then
    raise exception 'La cantidad debe ser mayor a cero';
  end if;

  select stock
    into v_stock
  from public.productos
  where id = p_producto_id
    and empresa_id = p_empresa_id
  for update;

  if not found then
    raise exception 'Producto no encontrado';
  end if;

  if p_tipo = 'ENTRADA' then
    v_nuevo_stock := v_stock + p_cantidad;
  elsif p_tipo = 'SALIDA' then
    v_nuevo_stock := v_stock - p_cantidad;
  else
    raise exception 'Tipo de movimiento invalido';
  end if;

  if v_nuevo_stock < 0 then
    raise exception 'Stock insuficiente';
  end if;

  update public.productos
    set stock = v_nuevo_stock
  where id = p_producto_id
    and empresa_id = p_empresa_id;

  insert into public.movimientos (
    empresa_id,
    producto_id,
    tipo,
    cantidad,
    observacion
  )
  values (
    p_empresa_id,
    p_producto_id,
    p_tipo,
    p_cantidad,
    nullif(trim(p_observacion), '')
  )
  returning id into v_movimiento_id;

  return query select v_movimiento_id, v_nuevo_stock;
end;
$$;

create or replace function public.crear_invitacion_empresa(
  p_empresa_id bigint,
  p_email text,
  p_rol text default 'EMPLEADO'
)
returns table (
  id uuid,
  token text,
  email text,
  rol text,
  expires_at timestamp with time zone
)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_invite public.invitaciones_empresa;
begin
  if not public.es_admin_empresa(p_empresa_id) then
    raise exception 'Solo un ADMIN puede invitar usuarios';
  end if;

  if p_rol not in ('ADMIN', 'SUPERVISOR', 'EMPLEADO') then
    raise exception 'Rol invalido';
  end if;

  insert into public.invitaciones_empresa (
    empresa_id,
    email,
    rol,
    created_by
  )
  values (
    p_empresa_id,
    lower(trim(p_email)),
    p_rol,
    auth.uid()
  )
  returning * into v_invite;

  return query
    select
      v_invite.id,
      v_invite.token,
      v_invite.email,
      v_invite.rol,
      v_invite.expires_at;
end;
$$;

create or replace function public.aceptar_invitacion_empresa(
  p_token text
)
returns table (
  empresa_id bigint,
  rol text
)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_invite public.invitaciones_empresa;
  v_user_email text;
begin
  select email
    into v_user_email
  from auth.users
  where id = auth.uid();

  if v_user_email is null then
    raise exception 'Usuario no autenticado';
  end if;

  select *
    into v_invite
  from public.invitaciones_empresa
  where token = p_token
    and estado = 'PENDIENTE'
  for update;

  if not found then
    raise exception 'Invitacion no encontrada o ya usada';
  end if;

  if v_invite.expires_at < now() then
    raise exception 'Invitacion vencida';
  end if;

  if lower(v_invite.email) <> lower(v_user_email) then
    raise exception 'Esta invitacion pertenece a otro correo';
  end if;

  if not public.empresa_habilitada(v_invite.empresa_id) then
    raise exception 'El negocio no esta activo';
  end if;

  insert into public.empresa_usuarios (
    empresa_id,
    user_id,
    email,
    rol
  )
  values (
    v_invite.empresa_id,
    auth.uid(),
    lower(v_user_email),
    v_invite.rol
  )
  on conflict (empresa_id, user_id)
  do update set
    email = excluded.email,
    rol = excluded.rol;

  update public.invitaciones_empresa
    set estado = 'ACEPTADA',
        accepted_by = auth.uid()
  where id = v_invite.id;

  return query select v_invite.empresa_id, v_invite.rol;
end;
$$;

